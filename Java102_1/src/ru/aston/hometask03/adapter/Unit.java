package ru.aston.hometask03.adapter;

public class Unit {

    private Integer value;


    public Unit() {
        this.value = value;
    }


    public Integer getValue() {
        return value;
    }

    public void setValue(Integer value) {
        this.value = value;
    }

    public Integer DoubleValue() {
        return value * 2;
    }
}
